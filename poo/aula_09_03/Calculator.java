package poo.aula_09_03;

public class Calculator {
    public static int sum(int x, int y) {
        return x + y;
    }

    public static int sub(int x, int y) {
        return x + y;
    }

    public static int mul(int x, int y) {
        return x * y;
    }
    
    public static double div(int x, int y) {
        return x / y;
    }
}

